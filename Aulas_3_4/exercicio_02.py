# 2 - Crie um programa em Python que solicite o valor em horas para o usuário,
# calcule emostre o valor em minutos, sabendo que 1 hora tem 60 minutos.

# Solicita ao usuário os valor em horas
valorHoras = int(input("Digite a um valor em horas: "))

# Realiza o calculo de horas para minutos
valorMinutos = valorHoras * 60

# Imprime para o usuário o valor em minutos
print("O valor em minutos é: %i" % (valorMinutos))
